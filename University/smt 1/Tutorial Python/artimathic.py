#operation,artimatchi on python

a = 5
b = 10

#operation addition +
total= a + b
print(a,'+',b,'=',total)

#operation subtraction -
total = a - b
print(a,'-',b,'=',total)

#operation muplication *
total = a * b
print(a,'*',b,'=',total)

#operation divison / 
total = a / b
print(a,'/',b,'=',total)

#operation eksponen **
total = a ** b
print(a,'**',b,'=',total)

#operation floor divison //
total = a // b
print(a,'//',b,'=',total)

#operation modulus %
total = a % b
print(a,'%',b,'=',total)

